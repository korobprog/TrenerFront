#!/bin/bash

# =====================================================
# Скрипт для запуска Docker контейнеров на Ubuntu
# =====================================================
# Инструкция по использованию:
# 1. Скопируйте этот файл на Ubuntu
# 2. Сделайте его исполняемым: chmod +x start-docker.sh
# 3. Запустите: ./start-docker.sh
# =====================================================

# Переходим в директорию с проектом
cd /path/to/your/project

# Запускаем Docker контейнеры в фоновом режиме
docker-compose up -d

# Выводим сообщение об успешном запуске
echo "Docker контейнеры запущены!"

# Ожидаем нажатия клавиши для продолжения
read -p "Нажмите Enter для выхода..."